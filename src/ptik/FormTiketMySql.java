    private void getAllData() {
        try {
            model.setRowCount(0);
            crud.bukaKoneksi();
            ArrayList<PemesananTiket> listPemesananTiket = crud.read();
            System.out.println(listPemesananTiket.size());
            for (PemesananTiket pt : listPemesananTiket) {
                model.addRow(new Object[]{
                    pt.getId(),
                    pt.getNamapemesan(),
                    pt.getTelp(),
                    pt.getNamakereta(),
                    pt.getKelas(),
                    pt.getStasiunasal(),
                    pt.getStasiuntujuan(),
                    pt.getTglberangkat(),
                    pt.getJamberangkat(),
                    pt.getHarga()
                });
            }
            crud.tutupKoneksi();
        } catch (SQLException | ClassNotFoundException ex) {
            JOptionPane.showMessageDialog(null, "Gagal Mengambil Data");
        }
    }

