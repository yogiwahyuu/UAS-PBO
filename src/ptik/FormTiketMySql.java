    private void tblKeretaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblKeretaMouseClicked
        try {
            // TODO add your handling code here:
            int baris = tblKereta.getSelectedRow();
            crud.bukaKoneksi();
            pt = crud.readById(Integer.parseInt(tblKereta.getValueAt(baris, 0).toString()));
            crud.tutupKoneksi();
            txtId.setText(Integer.toString(pt.getId()));
            txtNamaPemesan.setText(pt.getNamapemesan());
            txtTelp.setText(pt.getTelp());
            txtKereta.setText(pt.getNamakereta());
            txtKelas.setText(pt.getKelas());
            txtAsal.setText(pt.getStasiunasal());
            txtTujuan.setText(pt.getStasiuntujuan());
            txtTgl.setText(pt.getTglberangkat());
            txtJam.setText(pt.getJamberangkat());
            txtHarga.setText(Integer.toString(pt.getHarga()));
 
        } catch (SQLException | ClassNotFoundException ex) {
            JOptionPane.showMessageDialog(null, "Ambil data gagal");
        }
    }//GEN-LAST:event_tblKeretaMouseClicked

