    private void btSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btSimpanActionPerformed
         PemesananTiket pemesananTiket;
        if (status.equals("add")) {
            try {
                if (txtNamaPemesan.getText().isEmpty() || txtTelp.getText().isEmpty() || txtKereta.getText().isEmpty() || txtKelas.getText().isEmpty() || txtAsal.getText().isEmpty() || txtTujuan.getText().isEmpty() || txtTgl.getText().isEmpty() || txtJam.getText().isEmpty() || txtHarga.getText().isEmpty()) {
                    JOptionPane.showMessageDialog(null, "Data harus diisi semua");
                } else {
                    crud.bukaKoneksi();
                    pemesananTiket = new PemesananTiket();
                    pemesananTiket.setNamapemesan(txtNamaPemesan.getText());
                    pemesananTiket.setTelp(txtTelp.getText());
                    pemesananTiket.setNamakereta(txtKereta.getText());
                    pemesananTiket.setKelas(txtKelas.getText());
                    pemesananTiket.setStasiunasal(txtAsal.getText());
                    pemesananTiket.setStasiuntujuan(txtTujuan.getText());
                    pemesananTiket.setTglberangkat(txtTgl.getText());
                    pemesananTiket.setJamberangkat(txtJam.getText());
                    pemesananTiket.setHarga(Integer.parseInt(txtHarga.getText()));
                    crud.create(pemesananTiket);
                    getAllData();
                    readData();
                    crud.tutupKoneksi();
                    JOptionPane.showMessageDialog(null, "Data berhasil disimpan");
                }
            } catch (SQLException | ClassNotFoundException ex) {
                JOptionPane.showMessageDialog(null, "Data gagal disimpan");
            }
        } else {
            try {
                if (txtNamaPemesan.getText().isEmpty() || txtTelp.getText().isEmpty() || txtKereta.getText().isEmpty() || txtKelas.getText().isEmpty() || txtAsal.getText().isEmpty() || txtTujuan.getText().isEmpty() || txtTgl.getText().isEmpty() || txtJam.getText().isEmpty() || txtHarga.getText().isEmpty()) {
                    JOptionPane.showMessageDialog(null, "Data harus diisi semua");
                } else {
                    crud.bukaKoneksi();
                    pemesananTiket = new PemesananTiket();
                    pemesananTiket.setId(Integer.parseInt(txtId.getText()));
                    pemesananTiket.setNamapemesan(txtNamaPemesan.getText());
                    pemesananTiket.setTelp(txtTelp.getText());
                    pemesananTiket.setNamakereta(txtKereta.getText());
                    pemesananTiket.setKelas(txtKelas.getText());
                    pemesananTiket.setStasiunasal(txtAsal.getText());
                    pemesananTiket.setStasiuntujuan(txtTujuan.getText());
                    pemesananTiket.setTglberangkat(txtTgl.getText());
                    pemesananTiket.setJamberangkat(txtJam.getText());
                    pemesananTiket.setHarga(Integer.parseInt(txtHarga.getText()));
                    crud.update(pemesananTiket);
                    getAllData();
                    readData();
                    crud.tutupKoneksi();
                    JOptionPane.showMessageDialog(null, "Data berhasil disimpan");
                }
            } catch (SQLException | ClassNotFoundException ex) {
                JOptionPane.showMessageDialog(null, "Data gagal disimpan");
            }
        }
    }//GEN-LAST:event_btSimpanActionPerformed

    private void btHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btHapusActionPerformed
        if (pt == null) {
            JOptionPane.showMessageDialog(null, "Pilih data dahulu");
        } else {
            try {
                int dialogButton = JOptionPane.YES_NO_OPTION;
                int dialogResult = JOptionPane.showConfirmDialog(null, "Hapus data?", "Warning", dialogButton);
                if (dialogResult == JOptionPane.YES_OPTION) {
                    crud.bukaKoneksi();
                    crud.delete(pt);
                    getAllData();
                    crud.tutupKoneksi();
                    pt = null;
                    kosongkanTeks();
                }
            } catch (SQLException | ClassNotFoundException ex) {
                JOptionPane.showMessageDialog(null, "Hapus data gagal");
            }
        }
    }//GEN-LAST:event_btHapusActionPerformed

